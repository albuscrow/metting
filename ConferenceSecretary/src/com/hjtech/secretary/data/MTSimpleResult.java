package com.hjtech.secretary.data;


/**
 * The Class MTSimpleResult.
 * 
 * @author albuscrow
 */
public class MTSimpleResult {
	
	/** The result. */
	int result;
	
	/** The details. */
	String details;
	
	/**
	 * Gets the details.
	 * 
	 * @return the details
	 */
	public String getDetails() {
		return details;
	}
	
	/**
	 * Sets the details.
	 * 
	 * @param details
	 *            the new details
	 */
	public void setDetails(String details) {
		this.details = details;
	}
	
	/**
	 * Gets the result.
	 * 
	 * @return the result
	 */
	public int getResult() {
		return result;
	}
	
	/**
	 * Sets the result.
	 * 
	 * @param result
	 *            the new result
	 */
	public void setResult(int result) {
		this.result = result;
	}
	
}
